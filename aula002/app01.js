//Operadores Relacionais 

/*>--> maior 
<--> menor 
>= -> maior ou igual 
<= ->  menor ou igual
== -> igual
!= -> diferente 
*/
console.log (10>20)
console.log (10==10)
console.log (10!=10)
console.log(10>=12)
console.log(8<=8)
console.log(6!= 9)

